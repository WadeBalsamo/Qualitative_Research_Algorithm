class TestStageTextReport(unittest.TestCase):

    def setUp(self):
        self.tmp = tempfile.mkdtemp()

    def tearDown(self):
        shutil.rmtree(self.tmp, ignore_errors=True)

    def _make_df(self) -> pd.DataFrame:
        return make_master_df(n_sessions=2, n_participants=2)

    def test_writes_file_to_correct_directory(self):
        df = self._make_df()
        sr = _make_stage_report(0)
        path = generate_stage_text_report(df, 0, _FRAMEWORK, sr, self.tmp)

        expected_dir = _paths.themes_dir(self.tmp)
        self.assertTrue(path.startswith(expected_dir),
                        f'Path {path!r} not under {expected_dir!r}')
        self.assertTrue(os.path.isfile(path))

    def test_filename_slug_for_vigilance(self):
        df = self._make_df()
        sr = _make_stage_report(0)
        path = generate_stage_text_report(df, 0, _FRAMEWORK, sr, self.tmp)
        self.assertEqual(os.path.basename(path), 'stage_vigilance.txt')

    def test_filename_slug_for_attention_regulation(self):
        """Multi-word short_name produces underscore-separated slug."""
        df = self._make_df()
        sr = _make_stage_report(2)
        path = generate_stage_text_report(df, 2, _FRAMEWORK, sr, self.tmp)
        self.assertEqual(os.path.basename(path), 'stage_attention_regulation.txt')

    def test_stage_name_and_definition_in_content(self):
        df = self._make_df()
        sr = _make_stage_report(0)
        path = generate_stage_text_report(df, 0, _FRAMEWORK, sr, self.tmp)
