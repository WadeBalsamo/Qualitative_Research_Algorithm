class TestGenerateAllStageTextReports(unittest.TestCase):

    def setUp(self):
        self.tmp = tempfile.mkdtemp()

    def tearDown(self):
        shutil.rmtree(self.tmp, ignore_errors=True)

    def _make_df(self) -> pd.DataFrame:
        return make_master_df(n_sessions=2, n_participants=2)

    def test_generates_one_file_per_stage(self):
        df = self._make_df()
        stage_reports = [_make_stage_report(i) for i in range(5)]

        paths = generate_all_stage_text_reports(df, _FRAMEWORK, stage_reports, self.tmp)

        self.assertEqual(len(paths), 5)
        for p in paths:
            self.assertTrue(os.path.isfile(p), f'Missing: {p}')

    def test_skips_entries_without_stage_id(self):
        df = self._make_df()
        good = _make_stage_report(0)
        bad = {'overall_prevalence': 0.2}  # no stage_id key

        paths = generate_all_stage_text_reports(df, _FRAMEWORK, [bad, good], self.tmp)
        self.assertEqual(len(paths), 1)

    def test_empty_list_returns_empty(self):
        df = self._make_df()
        paths = generate_all_stage_text_reports(df, _FRAMEWORK, [], self.tmp)
        self.assertEqual(paths, [])

    def test_partial_framework_only_matching_stages_written(self):
        """Only stages present in stage_reports are written; framework keys irrelevant."""
        df = self._make_df()
        # Only supply stages 0 and 2
        stage_reports = [_make_stage_report(0), _make_stage_report(2)]

        paths = generate_all_stage_text_reports(df, _FRAMEWORK, stage_reports, self.tmp)
        self.assertEqual(len(paths), 2)
        basenames = {os.path.basename(p) for p in paths}
        self.assertIn('stage_vigilance.txt', basenames)
        self.assertIn('stage_attention_regulation.txt', basenames)


if __name__ == '__main__':
    unittest.main()
