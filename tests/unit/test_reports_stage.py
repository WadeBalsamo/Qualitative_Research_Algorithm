def _make_stage_report(stage_id: int, trend: float = 0.01,
                       n_total: int = 100, n_stage: int = 30,
                       with_exemplars: bool = True,
                       with_codes: bool = True) -> dict:
    """Minimal per-stage analysis dict as generated by the analysis runner."""
    prev_by_session = {str(s): round(n_stage / n_total + s * 0.01, 3) for s in range(1, 5)}
    prev_by_participant = {'P01': 0.35, 'P02': 0.25, 'P03': 0.10}

    exemplars = []
    if with_exemplars:
        exemplars = [
            {
                'participant_id': 'P01',
                'session_id': 'c1s1',
                'confidence': 0.88,
                'text': f'This is a stage {stage_id} exemplar utterance about pain.',
                'justification': 'Clearly matches the stage definition.',
            },
            {
                'participant_id': 'P02',
                'session_id': 'c1s2',
                'confidence': 0.75,
                'text': f'Another stage {stage_id} exemplar about awareness.',
                'justification': '',
            },
        ]

    co_codes = []
    if with_codes:
        co_codes = [
            {'code_id': 'body_awareness', 'lift': 2.3, 'count': 14},
            {'code_id': 'present_moment', 'lift': 1.8, 'count': 9},
        ]

    return {
        'stage_id': stage_id,
        'overall_prevalence': n_stage / n_total,
        'n_segments_total': n_total,
        'n_segments_this_stage': n_stage,
        'longitudinal_trend': trend,
        'prevalence_by_session_number': prev_by_session,
        'prevalence_by_participant': prev_by_participant,
        'top_exemplars': exemplars,
        'co_occurring_codes': co_codes,
    }


